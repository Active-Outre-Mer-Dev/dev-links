    <h1>Developper Essentials</h1>
    <p>Here are some tools that are considered essential for every developer. Here are a few of the necessary tools for every developer:</p>
    <hr>
        <h3>Text editor or Integrated Development Environment (IDE)</h3>
        <p>A text editor or IDE is where developers write, edit, and manage their code. Some popular text editors and IDEs include Visual Studio Code, Sublime Text, and IntelliJ IDEA.</p>
        <h3>Version Control System (VCS)</h3>
        <p>A VCS helps developers track changes to their code over time and collaborate with other team members. Git is one of the most popular VCSs used by developers today.</p>
        <h3>Command Line Interface (CLI)</h3>
        <p>The CLI is a text-based interface used to enter commands to interact with a computer's operating system. It is an essential tool for developers who need to perform tasks that aren't available through graphical user interfaces (GUIs).</p>
        <h3>Web Browser</h3>
        <p>A web browser is used to test and debug web applications during development. Chrome, Firefox, and Safari are popular web browsers used by developers.</p>
        <h3>Debugging tools</h3>
        <p>Debugging tools help developers identify and fix errors in their code. Chrome DevTools and Firefox Developer Tools are popular debugging tools.</p>
        <h3>Package manager</h3>
        <p>A package manager helps manage dependencies in a project. Popular package managers for JavaScript include npm and Yarn.</p>
        <h3>Task runner or build tool</h3>
        <p>A task runner or build tool automates repetitive tasks in a project, such as compiling code or compressing files. Some popular tools include Grunt, Gulp, and webpack.</p>
        <h3>Code libraries and frameworks</h3>
        <p>Code libraries and frameworks help developers write code more efficiently by providing pre-written code for common tasks. Popular libraries and frameworks include jQuery, React, and Angular (You can find a few links redirecting to said libraries in the lower section of this document).</p>
